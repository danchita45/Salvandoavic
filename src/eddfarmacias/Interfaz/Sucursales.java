    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        String sucursal = txts.getText();
        String clave = txtcl.getText();
        String codigo = txtcps.getText();
        if (sucursal.length() > 0) {
            JOptionPane.showMessageDialog(this, "Coloque una Sucursal");
        } else {
            if (clave.length() > 0) {
                JOptionPane.showMessageDialog(this, "Coloque Clave");
            } else {
                if (codigo.length() > 0) {
                    JOptionPane.showMessageDialog(this, "Seleccion un Codigo Postal");
                } else {

                    //asignamos valores
                    eddfarmacias.Localidad suc = new Localidad();
                    suc.setClave(txtcl.getText());
                    suc.setCodigoPostal(txtcps.getText());
                    suc.setNombre(txts.getText());
                    //hacemos nodo 
                    NodoLista nls = new NodoLista(txtcl.getText(), suc);
                    //armamos etqs
                    String[] etqs = new String[3];
                    etqs[0] = FarmaciaC.getSelectedItem().toString();
                    etqs[1] = CiudadC.getSelectedItem().toString();
                    etqs[2] = txtcl.getText();
                    //insertamos multilista
                    mul.inserta(etqs, 0, nls, mul.getR());
                    EscrLect a = new EscrLect();
                    try {
                        a.InsertarnuevaLista(mul);
                        //falta agregar al archivo para guardarlo
                    } catch (IOException ex) {
                        Logger.getLogger(Sucursales.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
        Productos p = new Productos(mul);
        p.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnAgregarActionPerformed

