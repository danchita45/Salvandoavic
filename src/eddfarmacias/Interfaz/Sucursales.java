    private void FarmaciaCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FarmaciaCActionPerformed
        CiudadC.removeAllItems();
        NodoLista aux;
        aux = mul.getR();
        if (FarmaciaC.getSelectedItem() != null) {
            while (aux.getEtiqueta() != FarmaciaC.getSelectedItem()) {
                aux = aux.getSig();
            }
        }

        if (aux != null) {
            if (aux.getAbajo() != null) {
                aux = aux.getAbajo();
                while (aux != null) {
                    CiudadC.addItem(aux.etiqueta);
                    aux = aux.getSig();
                }
            }
        }

        if (CiudadC.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No hay Ciudades, imposible agregar, agregue una Ciudad");
            btnAgregar.setEnabled(false);
            this.dispose();
        }
    }//GEN-LAST:event_FarmaciaCActionPerformed

