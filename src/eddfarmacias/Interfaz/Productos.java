    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        String codigo = txtcp.getText();
        String excistencia = txtep.getText();
        String nombre = txtnop.getText();
        String precio = txtpp.getText();
        if (codigo.length() > 0) {
            JOptionPane.showMessageDialog(this, "Coloque Codigo Producto");
        } else {
            if (excistencia.length() > 0) {
                JOptionPane.showMessageDialog(this, "Coloque numero de Excistencia");
            } else {
                if (nombre.length() > 0) {
                    JOptionPane.showMessageDialog(this, "Coloque Nombre de Producto");
                } else {
                    if (precio.length() > 0) {
                        JOptionPane.showMessageDialog(this, "Coloque Precio del Producto");
                    } else {

                        //asignamos valores
                        eddfarmacias.Producto p = new Producto(txtcp.getText(), txtnop.getText(), Double.parseDouble(txtpp.getText()), Integer.parseInt(txtep.getText()));
                        //hacemos nodo
                        NodoLista nls = new NodoLista(txtcp.getText(), p);
                        //armamos etqs
                        String[] etqs = new String[4];
                        etqs[0] = FarmaciaC.getSelectedItem().toString();
                        etqs[1] = CiudadC.getSelectedItem().toString();
                        etqs[2] = SucuralC.getSelectedItem().toString();
                        etqs[3] = txtcp.getText();
                        //insertamos multilista
                        mul.inserta(etqs, 0, nls, mul.getR());
                        Interfaz m = new Interfaz();
                        m.ml = mul;
                        EscrLect a = new EscrLect();
                        try {
                            a.InsertarnuevaLista(mul);
                        } catch (IOException ex) {
                            Logger.getLogger(Producto.class.getName()).log(Level.SEVERE, null, ex);
                        }

                        m.setVisible(true);
                        this.dispose();
                        //falta agregar al archivo para guardarlo
                    }
                }
            }
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

