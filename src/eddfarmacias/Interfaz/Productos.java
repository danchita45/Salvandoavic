    public void ponerCombox(MultiLista m) {
        CiudadC.removeAllItems();
        NodoLista aux;
        NodoLista auxc = null;
        int bnd = 0;

        aux = m.getR();
        if (FarmaciaC.getSelectedItem() != null) {
            while (aux.getEtiqueta() != FarmaciaC.getSelectedItem()) {
                aux = aux.getSig();

            }
        }
        if (aux != null) {
            if (aux.getAbajo() != null) {
                aux = aux.getAbajo();
                while (aux != null) {
                    CiudadC.addItem(aux.etiqueta);
                    auxc = aux;
                    aux = aux.getSig();

                }
            }
        }
        if (auxc != null) {
            while (auxc.getAnt() != null) {

                auxc = auxc.getAnt();
            }
        }

        if (CiudadC.getSelectedItem() != null) {

            while (bnd != 1) {
                if (auxc.getEtiqueta() == CiudadC.getSelectedItem()) {
                    bnd = 1;
                    AuxGen = auxc;
                } else {
                    if (auxc.getSig() != null) {
                        auxc = auxc.getSig();
                    } else {
                        break;
                    }
                }
            }
        }

        SucuralC.removeAllItems();
        if (AuxGen != null) {
            if (AuxGen.getAbajo() != null) {
                AuxGen = AuxGen.getAbajo();
                while (AuxGen != null) {
                    SucuralC.addItem(AuxGen.etiqueta);
                    AuxGen = AuxGen.getSig();
                }
            }
        }
    }

    public void llenaultimo(MultiLista ml) {

        NodoLista aux;
        NodoLista auxc = null;
        int bnd = 0;

        aux = ml.getR();
        if (FarmaciaC.getSelectedItem() != null) {
            while (aux.getEtiqueta() != FarmaciaC.getSelectedItem()) {
                aux = aux.getSig();

            }
        }
        if (aux != null) {
            if (aux.getAbajo() != null) {
                aux = aux.getAbajo();
                while (aux != null) {
                    auxc = aux;
                    aux = aux.getSig();

                }
            }
        }

        if (auxc != null) {
            while (auxc.getAnt() != null) {
                auxc = auxc.getAnt();
            }
        }

        if (CiudadC.getSelectedItem() != null) {

            while (bnd != 1) {
                if (auxc.getEtiqueta() == CiudadC.getSelectedItem()) {
                    bnd = 1;
                    AuxGen = auxc;
                } else {
                    if (auxc.getSig() != null) {
                        auxc = auxc.getSig();
                    } else {
                        break;
                    }
                }
            }
        }

        SucuralC.removeAllItems();
        if (AuxGen != null) {
            if (AuxGen.getAbajo() != null) {
                AuxGen = AuxGen.getAbajo();
                while (AuxGen != null) {
                    SucuralC.addItem(AuxGen.etiqueta);
                    AuxGen = AuxGen.getSig();
                }
            }
        }
    }

